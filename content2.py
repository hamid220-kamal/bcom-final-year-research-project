            "The core legislation governing Central taxes is the Central Goods and Services Tax (CGST) "
            "Act, 2017. This Act contains 174 sections divided into 21 chapters, detailing everything "
            "from the scope of supply, time and value of supply, input tax credit mechanisms, "
            "registration requirements, and the filing of returns, to offenses, penalties, and appeals. "
            "It establishes the legal backbone for the collection of Central Tax on intrastate supplies.\n"
            "Parallel to the CGST Act are 31 State Goods and Services Tax (SGST) Acts, one for each "
            "State and Union Territory with its own legislature (such as Delhi and Puducherry). The "
            "Telangana SGST Act, 2017 mirrors the provisions of the CGST Act almost verbatim. This "
            "mirroring is a critical design feature intended to ensure that a taxpayer in Shadnagar "
            "operating under the SGST faces the exact same legal definitions and procedures as one "
            "operating under the CGST.\n"
            "Interstate transactions, however, fall exclusively under the domain of the Union Government "
            "via the Integrated Goods and Services Tax (IGST) Act, 2017. It mandates the levy of IGST "
            "on all inter-state supplies (including imports, which are treated as inter-state supplies). "
            "The IGST mechanism ensures that the tax flows to the destination state, fulfilling the "
